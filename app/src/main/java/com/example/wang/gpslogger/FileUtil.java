    /**
     * 确保文件存在 否则创建它
     *
     * @param file  目标检查的文件
     * @param isDir 目标文件是否为目录
     * @return
     */
    public static boolean ensureFileExist(File file, boolean isDir) {
        if ((!isDir && file.isDirectory()) || (isDir && file.isFile())) {
            if (!file.delete()) {
                Log.e("FileUtil", "cannot delete file : " + file);
                return false;
            }
        }

        if (file.exists()) {
            return true;
        }

        if (ensureFileExist(file.getParentFile(), true)) {
            if (isDir) {
                return file.mkdir();
            } else {
                try {
                    return file.createNewFile();
                } catch (Exception e) {
                    e.printStackTrace();
                    return false;
                }
            }
        } else {
            return false;
        }
    }


    /**
     * Bitmap 转成文件
     *
     * @param bitmap
     * @return
     */
    public static File bitmapToFile(Context context, Bitmap bitmap) {
        File file = FileUtil.getRandomFilePath(context, "png", false);
        if (file == null || !file.exists()) return null;

        FileOutputStream fos = null;
        try {
            fos = new FileOutputStream(file);
            bitmap.compress(Bitmap.CompressFormat.PNG, 70, fos);
            return file;
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            if (fos != null) {
                try {
                    fos.close();
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
        }
        return file;
    }

